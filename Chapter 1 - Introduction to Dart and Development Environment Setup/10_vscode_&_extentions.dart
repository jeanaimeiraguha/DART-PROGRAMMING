// download and install VS Code

/*

  To download and install Visual Studio Code (VS Code), you can follow these steps:

  1. Visit the official Visual Studio Code website: https://code.visualstudio.com/

  2. On the homepage, you will see a "Download for" section. Click on the download button that corresponds to your operating system (Windows, macOS, or Linux).

  3. Once the download is complete, locate the downloaded file and run the installer.

  4. Follow the installation wizard prompts to set up Visual Studio Code on your system.

  5. During the installation, you may be asked to select additional components or customize the installation settings. You can choose the desired options based on your preferences.

  6. After the installation is complete, you can launch Visual Studio Code from the desktop shortcut or the Start menu.

  7. Upon launching VS Code, you will see the welcome screen. You can explore the available features or extensions, or you can proceed directly to writing code.

  8. On the left sidebar, click on the "Extensions" icon, or press Ctrl+Shift+X (Windows/Linux) or Cmd+Shift+X (macOS) to open the Extensions view.

  9. In the search bar at the top of the Extensions view, type "Dart".

  10. From the search results, locate the "Dart" extension published by the "Dart Code" team. It should have a green Dart logo.

  11. Click on the "Install" button next to the Dart extension to begin the installation process.

  12. After the installation is complete, you will see an "Enable" button. Click on it to enable the Dart extension.

  13. Once enabled, the Dart extension will provide various features and tools for Dart and Flutter development in Visual Studio Code.


That's it! You have successfully downloaded and installed Visual Studio Code on your computer. You can now start using it as your code editor and take advantage of its features and extensions for your programming projects.
*/