/*

  The main control flow statements in Dart are:

  If-Else Statements:

    The if statement allows you to execute a block of code conditionally based on a Boolean expression.
    The if-else statement extends the if statement by providing an alternative block of code to execute when the condition is not met.
  
  Switch Statements:

    The switch statement allows you to execute different blocks of code based on the value of a variable or an expression.
    It provides an alternative to multiple if-else statements and offers more concise code for handling multiple cases.
  
  For Loop:

    The for loop allows you to execute a block of code repeatedly for a specific number of iterations.
    It consists of an initialization statement, a condition for iteration, an expression for incrementing or decrementing, and the loop body.
  
  While and Do-While Loops:

    The while loop executes a block of code repeatedly as long as a condition remains true.
   The do-while loop is similar to the while loop but guarantees that the loop body is executed at least once before checking the condition.
  
  Break and Continue Statements:

    The break statement is used to exit a loop or switch statement prematurely.
    The continue statement is used to skip the remaining code in a loop iteration and proceed to the next iteration.
  
  Ternary Operator:

    The ternary operator (condition ? expr1 : expr2) provides a concise way to make a decision and assign a value based on a condition.
  
  These control flow statements allow you to structure your code, handle different scenarios, and make your programs more flexible and dynamic. By understanding and utilizing these statements effectively, you can control the flow of execution in your Dart programs and make them more powerful and responsive to different conditions and inputs.

*/

  
