void main(List<String> args) {
  
  // Equality and relational operators

  
  /* 
    Equality and relational operators are used to compare values and determine the relationship between them. These operators return a Boolean value (true or false) based on the comparison result. 
  
    Here are the commonly used equality and relational operators in Dart:
  */


  // 1. Equality Operators:

  // Equal to (==): Returns true if the operands are equal, otherwise false.
  // Not equal to (!=): Returns true if the operands are not equal, otherwise false.

  var a = 5;
  var b = 7;
  print(a == b);  // Output: false
  print(a != b);  // Output: true


  // Relational Operators:
  // 1. Greater than (>)
  // Returns true if the left operand is greater than the right operand, otherwise false.

  print(a > b);   // Output: false

  
  // 2. Less than (<): 
  // Returns true if the left operand is less than the right operand, otherwise false.

  print(a < b);   // Output: true


  // 3. Greater than or equal to (>=): 
  // Returns true if the left operand is greater than or equal to the right operand, otherwise false.

  print(a >= b);  // Output: false


  // 4. Less than or equal to (<=): 
  // Returns true if the left operand is less than or equal to the right operand, otherwise false.

  print(a <= b);  // Output: true


  /*

    These operators are useful when you need to compare values, make decisions based on conditions, or create conditional expressions. The result of the comparison is always a Boolean value (true or false), indicating the outcome of the comparison operation.
  
  */

}