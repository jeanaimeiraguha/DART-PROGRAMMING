  // Unary Operators

  // Unary operators are operators that perform operations on a single operand, which can be either a variable, a constant, or an expression. They manipulate the value of the operand in various ways. Dart supports several unary operators:


  // Increment Operators ++

  // 1. Postfix Increment Operator

    var a = 5;
    a++; // Postfix increment
    print(a);  // Output: 6

  // 2. Prefix Increment Operator

    var b = 10;
    ++b; // Prefix increment
    print(b);  // Output: 11


  // Decrement Operator --

  // 1. Postfix Decrement Operator

    var x = 5;
    x--; // Postfix increment
    print(x);  // Output: 6

  
  // 2. Prefix Decrement Operator

    var y = 10;
    --y; // Prefix increment
    print(y);  // Output: 11

