
// Different types of loops in Dart:

// for loop: Executes a block of code for a specific number of times based on a defined condition.

// while loop: Continuously executes a block of code as long as a given condition is true.

// do-while loop: Executes a block of code at least once and then repeats the execution as long as a given condition is true.

// for-in loop: Iterates over the elements of a collection (such as a list or a map) and executes a block of code for each element.

// These different types of loops provide flexibility in controlling the flow of execution and allow programmers to handle various scenarios where repetitive actions or iterations are required.